from Base import BaseLinkedList,Node


class LinkedList(BaseLinkedList):
    def partition_list(self, x):
        dummy1 = Node(0)
        dp1 = dummy1
        dummy2 = Node(0)
        dp2 = dummy2

        current = self.head
        while current:
            if current.value < x:
                dp1.next = current
                dp1= dp1.next
            else:
                dp2.next = current
                dp2 = dp2.next

            current = current.next
        
        dp2.next = None

        dp1.next = dummy2.next

        self.head = dummy1.next
       





